
public class SimpleMathOperations {

    public static int add(final int theX, final int theY) {
        return theX + theY;
    }
    
    public static int multiply(final int theX, final int theY) {
        return theX * theY;
    }
    
    public static int subtract(final int theX, final int theY) {
        return theX - theY;
    }
    
    public static int divide(final int theX, final int theY) {
        return theX / theY;
    }
    
}
